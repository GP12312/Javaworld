package PsU.Command;

public class Stereo {

	public void on() {
		System.out.println("Stereo turned ON ");
	}
	
	public void oFF() {
		System.out.println("Stereo turned OFF");
	}
	
	public void setVolume(int i) {
		System.out.println("Stereo Volume set to  " + i);
		
	}
	
	public void setStereoToRadio() {		
        System.out.println("Stereo is set to play Radio");		
	}
	
	public void setStereoToDVD() {		
        System.out.println("Stereo is set to play DVD");		
	}
	
	public void setStereoToCD() {		
        System.out.println("Stereo is set to play CD");		
	}
	
	
	
	
	
	
	
}
